export const generateBigRoadData = (columm) => {
  const columns = [];
  let counterId = 1;

  for (let i = 1; i <= columm; i++) {
    let columnData = [];
    for (let row = 1; row <= 6; row++) {
      columnData.push({
        id: counterId++,
        columnPosition: i,
        rowPosition: row,
        columnCount: null,
      });
    }

    columns.push({ [`col${i}`]: columnData });
  }

  return columns;
};

